/**
 * מחפש את אינדקס תחילת פרשה במערך טוקנים.
 * משווה רצף של 3-5 מילים פותחות לזיהוי חד-משמעי.
 *
 * @param {Array} tokens - טוקנים מ-tokenizeText (לאחר ניקוי הטקסט)
 * @param {string} parashaName - שם הפרשה
 * @param {number} startFromIdx - אינדקס התחלה לחיפוש
 * @returns {number} אינדקס תחילת הפרשה (-1 אם לא נמצא)
 */
function findParashaStart(tokens, parashaName, startFromIdx = 0) {
    const opening = PARASHA_OPENINGS[parashaName];
    if (!opening) {
        console.warn('No opening words for parasha: ' + parashaName);
        return -1;
    }
    const norm = (s) => s.replace(/[֑-ׇ־]/g, '').replace(/יקוק/g, 'יהוה');
    const normOpening = opening.map(norm);

    for (let i = startFromIdx; i < tokens.length - opening.length; i++) {
        if (tokens[i].type !== 'word') continue;
        let opIdx = 0, tIdx = i, matched = true;
        while (opIdx < normOpening.length && tIdx < tokens.length) {
            const tok = tokens[tIdx];
            if (tok.type !== 'word') { tIdx++; continue; }
            if (norm(tok.value) !== normOpening[opIdx]) { matched = false; break; }
            opIdx++; tIdx++;
        }
        if (matched && opIdx === normOpening.length) return i;
    }
    return -1;
}

