// פיצול לשורות. השורה הראשונה מתחילה ב-|align (לא ב-|-).
// נטפל בשורה הראשונה בנפרד אם היא לא מופרדת ב-|-.
const allBlocks = wikitext.split(/\n\|-[^\n]*/);
const rows = [];
let totalWords = 0;

for (const block of allBlocks) {
    if (!block.trim()) continue;
    const cells = [];
    // תא אחד: |align=X colspan=Y [width=Z%]|content
    const cellRegex = /\|align=(right|center|left)\s+colspan=(\d+)(?:\s+width=(\d+)%)?\|([^\n]+)/g;
    let m;
    while ((m = cellRegex.exec(block)) !== null) {
        const align = m[1];
        const colspan = parseInt(m[2]);
        // width מתקבל מ-width=X% או מ-colspan/60*100
        const width = m[3] ? parseInt(m[3]) : Math.round((colspan / 60) * 100);
        let text = m[4];
        // התבנית {{מ:פסק}} מציינת פסק (׀) ומפרידה בין מילים. החלף ברווח קודם.
        text = text.replace(/\{\{מ:פסק\}\}/g, ' ');
        text = text.replace(/\{\{[^}]*\}\}/g, '');
        text = text.replace(/<[^>]*>/g, '');
        text = text.replace(/\[\[[^\]]*\]\]/g, '');
        text = text.replace(/־/g, ' ');
        text = text.replace(/[֑-ׇ]/g, '');
        text = text.replace(/׃/g, '');
        text = text.replace(/\s+/g, ' ').trim();
        const words = text.split(' ').filter(w => w);
        cells.push({ align, width, count: words.length, words });
    }
    if (cells.length > 0) {
        rows.push(cells);
        for (const c of cells) totalWords += c.count;
    }
}

